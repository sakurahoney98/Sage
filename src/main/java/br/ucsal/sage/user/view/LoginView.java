	private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {// GEN-FIRST:event_jButtonLoginActionPerformed
		controller = new UserController();

		if (controller.validateLogin(conect.getConexao(), jTextFieldEmail.getText(), jPasswordFieldSenha.getText())) {
			User usuario = controller.getAllUserData(conect.getConexao(), jTextFieldEmail.getText());

			if (usuario.getTipo_usuario() == 2) {
				PageCollectorView.setConect(conect.getConexao());
				PageCollectorView.setUsuario(usuario);
				new PageCollectorView().main(null);
				this.dispose();
			} else {
				PageRequesterView.setConect(conect.getConexao());
				PageRequesterView.setUsuario(usuario);
				new PageRequesterView().main(null);
				this.dispose();
			}

		}

		else {
			jLabelAvisoErro.setText("Login incorreto.");
			jPasswordFieldSenha.setText("");
		}

	}// GEN-LAST:event_jButtonLoginActionPerformed


	private void jButtonChamadosMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jButtonChamadosMouseClicked
		RequestOptionsView.setConect(conect);
		RequestOptionsView.setUsuario(usuario);
		new RequestOptionsView().main(null);
		this.dispose();
	}// GEN-LAST:event_jButtonChamadosMouseClicked

	private void jButtonEditarPerfilMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jButtonEditarPerfilMouseClicked
		EditProfileView.setUsuario(usuario);
		EditProfileView.setConect(conect);
		new EditProfileView().main(null);
		this.dispose();
	}// GEN-LAST:event_jButtonEditarPerfilMouseClicked

	private void jButtonSairMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_jButtonSairMouseClicked
		new LoginView().main(null);
		this.dispose();
	}// GEN-LAST:event_jButtonSairMouseClicked

	private void jButtonTelaInicialMouseClicked(java.awt.event.MouseEvent evt) throws SQLException {
		if (usuario.getTipo_usuario() == 1)
			new PageRequesterView().main(null);
		else
			new PageCollectorView().main(null);

		this.dispose();
	}

